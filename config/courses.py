def get_all_courses():
    all_courses = []
    for group, courses in RUNNING_COURSES.items():
        for course in courses:
            course["group"] = group
            all_courses.append(course)
    return all_courses


def get_course_by_id(course_id: str):
    for courses in RUNNING_COURSES.values():
        for course in courses:
            if course["id"] == course_id:
                return course
    return None
